package classes;


public class clsAddition {
    
    public static int sumar_numeros(int numero_uno, int numero_dos){
        
        int suma1 = numero_uno + numero_dos;
        
        return suma1;
    }
    
    public static int sumar_segunda_fila(int numero_uno, int numero_dos, int numero_tres){
        
        int suma2 = numero_uno + numero_dos + numero_tres;
        
        return suma2;
    }
    
    public static int sumar_tres_numeros(int numero_uno, int numero_dos, int numero_tres, int numero_cuatro, int numero_cinco, int numero_cseisuatro){
        
        int suma = numero_uno + numero_dos;
        
        return suma;
    }
    
    public static int sumar_total(int numero_uno, int numero_dos, int numero_tres, int numero_cuatro, int numero_cinco, int numero_seis, int numero_siete, int numero_ocho){
        
        int suma_total = numero_uno + numero_dos + numero_tres + numero_cuatro + numero_cinco + numero_seis + numero_siete + numero_ocho;
        
        return suma_total;
    }
}
